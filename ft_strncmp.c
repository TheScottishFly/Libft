#include "libft.h"

int		ft_strncmp(const char *s1, const char *s2, size_t n)
{
	while (*(t_byte *)s1 == *(t_byte *)s2 && n--)
	{
		if (*s1 == '\0' || *s2 == '\0')
			return (0);
		s1++;
		s2++;
	}
	if (!n)
		return ((*(t_byte *)--s1 - *(t_byte *)--s2));
	return (*(t_byte *)s1 - *(t_byte *)s2);
}
