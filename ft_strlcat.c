#include "libft.h"

size_t		ft_strlcat(char *dst, const char *src, size_t size)
{
	char		*dest;
	const char	*source;
	size_t		i;
	size_t		len;

	dest = dst;
	source = src;
	i = size;
	while (*dest != '\0' && i-- != 0)
		dest++;
	len = dest - dst;
	i = size - len;
	if (i == 0)
		return (len + ft_strlen(src));
	while (*source != '\0')
	{
		if (i != 1)
		{
			*dest++ = *source;
			i--;
		}
		source++;
	}
	*dest = '\0';
	return (len + (source - src));
}
