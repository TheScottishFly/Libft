#include "libft.h"

int		ft_wordcount(const char *str, char delim)
{
	int		i;
	int		j;

	i = 0;
	j = 0;
	while (str[i])
	{
		if (str[i - 1] != delim && (str[i] == delim || str[i]))
			j++;
		i++;
	}
	return (j);
}
